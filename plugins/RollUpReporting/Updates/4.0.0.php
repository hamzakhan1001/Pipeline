namespace Piwik\Plugins\RollUpReporting;

use Piwik\Updater;
use Piwik\Updates as PiwikUpdates;
use Piwik\Updater\Migration\Factory as MigrationFactory;

/**
 * Update for version 4.0.0
 */
class Updates_4_0_0 extends PiwikUpdates
{
    /**
     * @var MigrationFactory
     */
    private $migration;

    public function __construct(MigrationFactory $factory)
    {
        $this->migration = $factory;
    }

    public function getMigrations(Updater $updater)
    {
        $migration1 = $this->migration->config->set('RollUpReporting', Configuration::FORCE_AGGREGATE_RAW_DATA_FOR_DAY, Configuration::DEFAULT_FORCE_AGGREGATE_RAW_DATA_FOR_DAY);
        $migration2 = $this->migration->config->set('RollUpReporting', Configuration::FORCE_AGGREGATE_RAW_DATA_FOR_DAY_SEGMENT, Configuration::DEFAULT_FORCE_AGGREGATE_RAW_DATA_FOR_DAY_SEGMENT);

        return array(
            $migration1,
            $migration2
        );
    }

    public function doUpdate(Updater $updater)
    {
        $updater->executeMigrations(__FILE__, $this->getMigrations($updater));
    }
}
